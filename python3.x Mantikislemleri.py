sayi1 = int(input("Birinci sayiyi giriniz :"))

sayi2 = int(input("Ikinci sayiyii giriniz :"))

if sayi1 == sayi2:
    print("birinci sayi ile ikinci sayi bir-birile esitdir")
    
if sayi1 != sayi2:
    print("birinci sayi ile ikinci sayi bir-birile esit degildir")

if sayi1 < sayi2:
    print("birinci sayi ikinci sayidan kucukdur")

if sayi1 > sayi2:
    print("birinci sayi ikinci sayidan buyukdur")

if sayi1 <= sayi2:
    print("birinci sayi kucuk esitdir")

if sayi1 >= sayi2:
    print("birinci sayi buyuk esitdir")
